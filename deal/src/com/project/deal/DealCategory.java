package com.project.deal;

public class DealCategory {
		private int bcNum;
		private String bcName;
		
		private int smNum;
		private String smName;
		public int getBcNum() {
			return bcNum;
		}
		public void setBcNum(int bcNum) {
			this.bcNum = bcNum;
		}
		public String getBcName() {
			return bcName;
		}
		public void setBcName(String bcName) {
			this.bcName = bcName;
		}
		public int getSmNum() {
			return smNum;
		}
		public void setSmNum(int smNum) {
			this.smNum = smNum;
		}
		public String getSmName() {
			return smName;
		}
		public void setSmName(String smName) {
			this.smName = smName;
		}
		
}
